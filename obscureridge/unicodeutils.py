def translate(text, style):
    text = str(text)
    try:
        tr = ASCII_TRANSLATIONS[style]
        newtext = ""
        for c in text:        
            index = COMMON_ASCII.find(c)            
            if index < 0:
                newtext += c
            else:
                newtext += tr[index]
        return newtext
    except:
        return text

